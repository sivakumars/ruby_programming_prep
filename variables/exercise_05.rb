# variables are completely scoped inside a method. That is, 
# variables defined inside a method are not available outside it, and
# variables defiined outside the method are not available inside the method.


# On the contrary, a block can access variables defined outside the block, 
# but the variables defined inside the block are not available outside it.

# blocks (identified as {}  or do...end ) are usually passed in as arguments to method invocations.